# Сап (String)
аты = "Айбек"
шаар = 'Бишкек'
print(f"Менин атым {аты}, мен {шаар} шаарында жашайм.")

# Сандар (Integers, Floats)
жашы = 30  # Бүтүн сан (integer)
бою = 1.75  # Ондук сан (float)
print(f"{аты} {жашы} жашта, бою {бою} метр.")

# Буль (Boolean)
студентпи = False
иштейби = True
print(f"Студентпи: {студентпи}, Иштейби: {иштейби}")

# Тизме (List) - өзгөрүлүүчү, иреттелген
суйгон_жемиштер = ["алма", "банан", "өрүк"]
суйгон_жемиштер.append("жүзүм") # Элемент кошуу
print(f"Сүйгөн жемиштерим: {суйгон_жемиштер[0]}, {суйгон_жемиштер}")

# Кортеж (Tuple) - өзгөрүлбөс, иреттелген
туулган_кун = (15, "март", 1993)
print(f"Туулган күнү: {туулган_кун[0]}-{туулган_кун[1]}, {туулган_кун[2]}-жыл")

# Сөздүк (Dictionary) - ачкыч-маани жуптары, иретсиз (Python 3.7+ иреттелген)
адам_жонундо = {
    "аты": "Айжан",
    "кесиби": "Программист",
    "стажы": 5
}
print(f"{адам_жонундо['аты']} - {адам_жонундо['кесиби']}, стажы {адам_жонундо['стажы']} жыл.")

# NoneType (Эч нерсе)
машинасы = None
if машинасы is None:
    print("Азырынча машинасы жок.")